"""
Introdução às funções (def) em python

Funções são trechos de código usados para replicar
determinada ação ao longo do código.

Elas podem receber valores para parâmetros(argumentos)
e retornar um valor especifico.

Por padrão, funções retornam None ( nada )


"""


def Print(a,b,c):
    print(f"Várias vezes{a}")
    print(f"Várias vezes{b}")
    print(f"Várias vezes{c}")
    
Print(1,2,3)